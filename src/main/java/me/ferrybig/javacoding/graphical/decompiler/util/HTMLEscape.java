package me.ferrybig.javacoding.graphical.decompiler.util;

public class HTMLEscape {

	public static String escapeHTML(String s) {
		StringBuilder out = new StringBuilder(Math.max(16, s.length()));
		for (int i = 0; i < s.length(); i++) {
			char c = s.charAt(i);
			if (c > 127 || c == '"' || c == '<' || c == '>' || c == '&') {
				out.append("&#");
				out.append((int) c);
				out.append(';');
			} else if(c == '\n') {
				out.append("<br>");
			} else {
				out.append(c);
			}
		}
		return out.toString();
	}
}
